package com.rimdev.gateway.ouputobject;

public class langob {

	String langtittle;
	String langcode;

	public langob() {
		super();
	}

	/**
	 * @return the langtittle
	 */
	public String getLangtittle() {
		return langtittle;
	}

	/**
	 * @param langtittle the langtittle to set
	 */
	public void setLangtittle(String langtittle) {
		this.langtittle = langtittle;
	}

	/**
	 * @return the langcode
	 */
	public String getLangcode() {
		return langcode;
	}

	/**
	 * @param langcode the langcode to set
	 */
	public void setLangcode(String langcode) {
		this.langcode = langcode;
	}

}
