package com.rimdev.accounting.outputobject;

public class textobject {
	
	int ivalue;
	String text;
	
	
	public textobject() {
		super();
	}
	
	public int getIvalue() {
		return ivalue;
	}
	public void setIvalue(int ivalue) {
		this.ivalue = ivalue;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
	

}
