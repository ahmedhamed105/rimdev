package com.rimdev.RES.Input;

public class Devpara {
	
	private String Dparameter;
	private String Dparavalue;
	
	
	
	
	public Devpara() {
		super();
	}
	
	
	public Devpara(String dparameter, String dparavalue) {
		super();
		Dparameter = dparameter;
		Dparavalue = dparavalue;
	}


	public String getDparameter() {
		return Dparameter;
	}
	public void setDparameter(String dparameter) {
		Dparameter = dparameter;
	}
	public String getDparavalue() {
		return Dparavalue;
	}
	public void setDparavalue(String dparavalue) {
		Dparavalue = dparavalue;
	}
	
	
	

}
